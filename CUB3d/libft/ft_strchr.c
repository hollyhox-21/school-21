#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != c)
	{
		s++;
		if (*s == '\0' && c == '\0')
			return ((char *)s);
		if (*s == '\0')
			return (NULL);
	}
	return ((char *)s);
}
