void	ft_toupper(char **c)
{
	int	i;

	i = 0;
	while ((*c)[i])
	{
		if ((*c)[i] >= 97 && (*c)[i] <= 122)
		{
			(*c)[i] = (*c)[i] - 32;
		}
		i++;
	}
}
